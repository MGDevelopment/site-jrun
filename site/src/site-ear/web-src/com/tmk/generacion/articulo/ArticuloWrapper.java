package com.tmk.generacion.articulo;

public class ArticuloWrapper {
	
	public int idArticulo;
	public String urlDetalle;
	public String altImagen;
	
	public int getIdArticulo() {
		return idArticulo;
	}
	public void setIdArticulo(int idArticulo) {
		this.idArticulo = idArticulo;
	}
	public String getUrlDetalle() {
		return urlDetalle;
	}
	public void setUrlDetalle(String urlDetalle) {
		this.urlDetalle = urlDetalle;
	}
	public String getAltImagen() {
		return altImagen;
	}
	public void setAltImagen(String altImagen) {
		this.altImagen = altImagen;
	}
	
	

}
